#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	t;

	t = 0;
	i = ft_strlen(s1);
	while (s2[t] && t < n)
	{
		s1[i] = s2[t];
		i++;
		t++;
	}
	s1[i] = '\0';
	return (s1);
}
